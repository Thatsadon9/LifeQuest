var isProd = process.env.NODE_ENV === "production" || process.env.VERCEL === "1";
function setSessionCookie(c, token) {
  setCookie(c, SESSION_COOKIE, token, {
    httpOnly: true,
    secure: isProd,
    sameSite: "Lax",
    path: "/",
    maxAge: Math.floor((sessionExpiryMs() - Date.now()) / 1e3)
  });
}
function clearSessionCookie(c) {
  deleteCookie(c, SESSION_COOKIE, { path: "/" });
}
function registerAuthRoutes(app, pool2) {
  app.post("/api/auth/register", rateLimit(8, 6e4), async (c) => {
    let body;
    try {
      body = await c.req.json();
    } catch {
      return c.json({ error: "Invalid JSON body" }, 400);
    }
    const email = String(body.email ?? "").trim();
    const password = String(body.password ?? "");
    const displayName = body.displayName?.trim();
    if (!validateEmail(email)) {
      return c.json({ error: "Invalid email address" }, 400);
    }
    const passwordError = validatePassword(password);
    if (passwordError) {
      return c.json({ error: passwordError }, 400);
    }
    const existing = await findUserByEmail(pool2, email);
    if (existing) {
      return c.json({ error: "An account with this email already exists" }, 409);
    }
    try {
      const user = await createUser(pool2, email, password, displayName);
      const { token } = await createSession(pool2, user.id);
      setSessionCookie(c, token);
      return c.json({
        user: { id: user.id, email: user.email, displayName: user.displayName }
      });
    } catch (err) {
      console.error("Register failed:", err);
      return c.json({ error: "Registration failed" }, 500);
    }
  });
  app.post("/api/auth/login", rateLimit(12, 6e4), async (c) => {
    let body;
    try {
      body = await c.req.json();
    } catch {
      return c.json({ error: "Invalid JSON body" }, 400);
    }
    const email = String(body.email ?? "").trim();
    const password = String(body.password ?? "");
    if (!email || !password) {
      return c.json({ error: "Email and password are required" }, 400);
    }
    const user = await findUserByEmail(pool2, email);
    if (!user || !await verifyPassword(password, user.passwordHash)) {
      return c.json({ error: "Invalid email or password" }, 401);
    }
    const { token } = await createSession(pool2, user.id);
    setSessionCookie(c, token);
    return c.json({
      user: { id: user.id, email: user.email, displayName: user.displayName }
    });
  });
  app.post("/api/auth/logout", async (c) => {
    const token = getCookie(c, SESSION_COOKIE);
    if (token) {
      await deleteSession(pool2, token);
    }
    clearSessionCookie(c);
    return c.json({ ok: true });
  });
  app.get("/api/auth/me", async (c) => {
    const token = getCookie(c, SESSION_COOKIE);
    if (!token) {
      return c.json({ user: null });
    }
    const user = await findUserBySessionToken(pool2, token);
    if (!user) {
      clearSessionCookie(c);
      return c.json({ user: null });
    }
    return c.json({ user });
  });
}

