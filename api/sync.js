import { createHash, randomBytes } from "node:crypto";
var SESSION_COOKIE = "lq_session";
var SESSION_DAYS = Number(process.env.SESSION_DAYS ?? 30);
function hashSessionToken(token) {
  return createHash("sha256").update(token).digest("hex");
}
function createSessionToken() {
  return randomBytes(32).toString("base64url");
}
function sessionExpiryMs() {
  return Date.now() + SESSION_DAYS * 24 * 60 * 60 * 1e3;
}
async function createSession(pool2, userId) {
  const token = createSessionToken();
  const tokenHash = hashSessionToken(token);
  const now = Date.now();
  const expiresAt = sessionExpiryMs();
  await pool2.query(
    `INSERT INTO sessions (token_hash, user_id, expires_at, created_at)
     VALUES ($1, $2, $3, $4)`,
    [tokenHash, userId, expiresAt, now]
  );
  return { token, expiresAt };
}
async function deleteSession(pool2, token) {
  await pool2.query(`DELETE FROM sessions WHERE token_hash = $1`, [
    hashSessionToken(token)
  ]);
}
async function deleteExpiredSessions(pool2) {
  await pool2.query(`DELETE FROM sessions WHERE expires_at < $1`, [Date.now()]);
}
async function findUserBySessionToken(pool2, token) {
  const result = await pool2.query(
    `SELECT u.id, u.email, u.display_name
     FROM sessions s
     JOIN users u ON u.id = s.user_id
     WHERE s.token_hash = $1 AND s.expires_at > $2`,
    [hashSessionToken(token), Date.now()]
  );
  const row = result.rows[0];
  if (!row) return null;
  return {
    id: String(row.id),
    email: String(row.email),
    displayName: String(row.display_name ?? "")
  };
}

