function requireUser(pool2) {
  return async (c, next) => {
    const token = getCookie(c, SESSION_COOKIE);
    if (!token) {
      return c.json({ error: "Unauthorized", code: "no_session" }, 401);
    }
    const user = await findUserBySessionToken(pool2, token);
    if (!user) {
      return c.json({ error: "Unauthorized", code: "invalid_session" }, 401);
    }
    c.set("userId", user.id);
    c.set("userEmail", user.email);
    c.set("userDisplayName", user.displayName);
    await next();
  };
}

// server/auth/password.ts
import { randomBytes as randomBytes2, scrypt, timingSafeEqual } from "node:crypto";
import { promisify } from "node:util";
var scryptAsync = promisify(scrypt);
var KEY_LEN = 64;
async function hashPassword(password) {
  const salt = randomBytes2(16).toString("hex");
  const derived = await scryptAsync(password, salt, KEY_LEN);
  return `scrypt:${salt}:${derived.toString("hex")}`;
}
async function verifyPassword(password, stored) {
  const parts = stored.split(":");
  if (parts.length !== 3 || parts[0] !== "scrypt") return false;
  const [, salt, hashHex] = parts;
  const derived = await scryptAsync(password, salt, KEY_LEN);
  const expected = Buffer.from(hashHex, "hex");
  if (derived.length !== expected.length) return false;
  return timingSafeEqual(derived, expected);
}
function validateEmail(email) {
  return /^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(email);
}
function validatePassword(password) {
  if (password.length < 8) return "Password must be at least 8 characters.";
  return null;
}

