/** Recursive map with string leaves (English literals widened to `string`). */
export type Dict = {
  readonly [K in keyof typeof en]: (typeof en)[K] extends readonly string[]
    ? readonly string[]
    : (typeof en)[K] extends string
      ? string
      : (typeof en)[K] extends object
        ? {
            readonly [P in keyof (typeof en)[K]]: (typeof en)[K][P] extends readonly string[]
              ? readonly string[]
              : (typeof en)[K][P] extends string
                ? string
                : (typeof en)[K][P] extends object
                  ? DictNested<(typeof en)[K][P]>
                  : (typeof en)[K][P];
          }
        : (typeof en)[K];
};

type DictNested<T> = {
  readonly [K in keyof T]: T[K] extends readonly string[]
    ? readonly string[]
    : T[K] extends string
      ? string
      : T[K] extends object
        ? DictNested<T[K]>
        : T[K];
};
